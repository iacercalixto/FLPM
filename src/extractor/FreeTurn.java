package extractor;

public class FreeTurn {

    private int begin, middle, end;

    public FreeTurn(int b, int m, int e) {
        this.begin = b;
        this.middle = m;
        this.end = e;
    }

    public int getBegin() {
        return this.begin;
    }

    public int getMiddle() {
        return this.middle;
    }

    public int getEnd() {
        return this.end;
    }

}
